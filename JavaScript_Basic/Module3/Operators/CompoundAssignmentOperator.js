// Compound Assignment Operators

// addition assignment +=
let add = 100;

add += 10;  
console.log('a : ',add);  // 110


// subtraction assignment -=
let sub = 100;

sub -= 10;  
console.log('sub : ',sub); // 90


// multiplication assignment *=
let m = 100;

m *= 10;  
console.log('m : ',m); // 1000



// Division assignment /=
let div = 100;

div /= 10;  
console.log('div : ',div); //10


// remainder assignment %=
let remainder = 100;

remainder %= 10;  
console.log('remainder : ',remainder); // 0



// power assignment **=.
let power = 100;

power **= 10;   
console.log('power : ',power); // 10 * 10^21


